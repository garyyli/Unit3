word = input('Enter a word: ')

for ch in word:
    if ch == 'a':
        print(ch.upper())
    if ch == 'e':
        print(ch.upper())
    if ch == 'i':
        print(ch.upper())
    if ch == 'o':
        print(ch.upper())
    if ch == 'u':
        print(ch.upper())
    else:
        print(ch)
